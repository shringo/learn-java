public class B_DataTypes {

    public static void main(String[] args) {

        // can only store one character
        char myGrade = 'F';
        String sentence = "One thing the world should know more about is AI sentience";

        // whole numbers ordered from smallest to largest capacity
        // underscores (_) add readability and doesn't change functionality
        byte age = 69;
        short time = 13_00;
        int population = 333_300_000;
        long homeworkAssignmentsDue = 9_223_372_036_854_775_807L;

        // decimal numbers
        float price = 9.99F;
        double PI = 3.14159265358979323846;

        // TRUE or FALSE
        boolean isInClass = true;

    }

}
