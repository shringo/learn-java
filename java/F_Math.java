public class F_Math {

    public static void main(String[] args) {

        int x = 10;
        int y = 7;
        int z;

        // ADDITION
        z = x + y;
        System.out.println("x + y:" + z);

        // SUBTRACTION
        z = x - y;
        System.out.println("x - y:" + z);

        // MULTIPLICATION
        z = x * y;
        System.out.println("x * y:" + z);

        // DIVISION
        z = x / y;
        System.out.println("x / y:" + z);

        // REMAINDER
        z = x % y;
        System.out.println("x % y:" + z);

    }

}
